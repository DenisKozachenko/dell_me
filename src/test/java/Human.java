public class Human {
    public int weight;
    public int age;
    public String firstName;
    public String lastName;
    public int height;

    public Human(int weight, int age, String firstName, String lastName, int height) {
        this.weight = weight;
        this.age = age;
        this.firstName = firstName;
        this.lastName = lastName;
        this.height = height;
    }
}
